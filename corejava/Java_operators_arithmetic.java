
class Java_operators_arithmetic{
    public static void main(String[] args) {
        int a=40;
        int b=21;
        System.out.println("Addition of variable a and b is:" + (a+b));    
        System.out.println("Subtraction of variable a and b is:"+(b-a));
        System.out.println("Division of variable a by b is:"+(a/b));
        System.out.println("Remainder of variables a and b is:"+(a%b));
    }
}
